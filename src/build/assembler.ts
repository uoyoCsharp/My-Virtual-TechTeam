import { existsSync, readFileSync, readdirSync } from "node:fs";
import path from "node:path";
import { parse as parseYaml } from "yaml";
import type { Manifest } from "../types/manifest.js";
import { loadSection } from "./section-loader.js";

const PKG_VERSION = "2.0.0";

function generatedHeader(timestamp?: string): string {
  const ts = timestamp ?? new Date().toISOString();
  return `<!-- GENERATED by mvtt v${PKG_VERSION} @ ${ts}. Manual edits will be overwritten. -->`;
}

function buildFrontmatter(fm: Record<string, string>): string {
  const lines = ["---"];
  for (const [key, value] of Object.entries(fm)) {
    if (value.includes("\n") || value.includes("'")) {
      lines.push(`${key}: "${value.replace(/"/g, '\\"')}"`);
    } else {
      lines.push(`${key}: '${value}'`);
    }
  }
  lines.push("---");
  return lines.join("\n");
}

export interface AssembleOptions {
  sourcesDir: string;
  timestamp?: string;
}

export function assembleFromManifest(
  manifestPath: string,
  options: AssembleOptions,
): string {
  const raw = readFileSync(manifestPath, "utf-8");
  const manifest: Manifest = parseYaml(raw);
  const skillDir = path.dirname(manifestPath);

  const parts: string[] = [];

  parts.push(generatedHeader(options.timestamp));
  parts.push(buildFrontmatter(manifest.frontmatter));
  parts.push("");

  for (const section of manifest.sections) {
    const content = loadSection(section, skillDir, options.sourcesDir);
    parts.push(content.trimEnd());
    parts.push("");
  }

  return parts.join("\n");
}

export function assembleAll(
  sourcesDir: string,
  kind: "skills" | "templates",
  timestamp?: string,
): Array<{ outputPath: string; content: string }> {
  const baseDir = path.resolve(sourcesDir, kind);
  const results: Array<{ outputPath: string; content: string }> = [];

  if (!existsSync(baseDir)) return results;

  for (const entry of readdirSync(baseDir)) {
    const manifestPath = path.join(baseDir, entry, "manifest.yaml");
    if (!existsSync(manifestPath)) continue;

    const raw = readFileSync(manifestPath, "utf-8");
    const manifest: Manifest = parseYaml(raw);

    const content = assembleFromManifest(manifestPath, {
      sourcesDir,
      timestamp,
    });

    results.push({ outputPath: manifest.output, content });
  }

  return results;
}
